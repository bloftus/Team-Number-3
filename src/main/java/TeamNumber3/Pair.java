package TeamNumber3;

class Pair {
	private int fileNum;
	private int positionNum;
	
	public Pair(int fileNum, int positionNum) {
		this.fileNum = fileNum;
		this.positionNum = positionNum;
	}
	
	public int getFileNum() {
		return this.fileNum;
	}
	
	public int getPositionNum() {
		return this.positionNum;
	}
}
